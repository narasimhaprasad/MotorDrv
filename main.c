/*
 * Timer ISR for inner current PWM loop
 * Speed : 40kHz
 */
void IntIcontrol_Timer1A(void)
{
	int64_t acc2 = 0x00;
	TimerIntClear(TIMER1_BASE, TIMER_TIMA_TIMEOUT);

	acc2 = (int64_t)(pidcontrol32(&ipid, ipid.ref - adc.ocurrent)*0x68280000);	//Q13.19 * Q13.19 = Q26.38
	conv.cycle = (int32_t)(acc2>>38);	//Q26.38 >> 19 : To convert acc2 into Q13.19 >> 19 to get final integer

	conv.cycle < 666 ? conv.cycle = 666 : conv.cycle;
	conv.cycle < 2666 ? conv.cycle = 2666 : conv.cycle;
	PWMPulseWidthSet(PWM1_BASE, PWM_GEN_1, conv.cycle);

	IntPendClear(INT_TIMER1A);
}

/*
 * PWM load ISR
 * Will trigger at middle of ON-cycle.
 * Preferably to do adc sampling and outer loop of PID control.
 * Run at 20kHz
 */
void PWM1IntHandler(void)
{
	int64_t acc1=0x00;
	PWMGenIntClear(PWM1_BASE, PWM_GEN_1, PWM_INT_CNT_LOAD|PWM_TR_CNT_LOAD);

	ADCSequenceDataGet(ADC0_BASE, 2, adc.buff);
	adcproc(&adc);
	acc1 = (int64_t)abs(adc.ivoltage)*pidcontrol32(&vpid, vpid.ref - adc.ovoltage);	//Q 13.19 * 13.19 = Q26.38
	ipid.ref = (int32_t)(acc1>>19);
	stateeval(sw2);

	IntPendClear(INT_PWM1_1);
}

/*
 * PID control algorithm
 * Implemented in 13.19 format for a resolution of 1.9073e-6
 * All elements in 13.19 format
 * Multiplication yields 26.38 format in 64 bit accumulator, shifted back by 19 and typecast to get 13.19 again
 * Clocked in at 1.26us
 */
static __inline int32_t pidcontrol32(pid_t *S, int32_t err)
{
	int64_t acc=0;
	int32_t out;

	acc = (int64_t)S->a0*err;
	acc += (int64_t)S->a1*S->state[0];

	out = (int32_t)(acc>>19);
	out += S->state[1];

	S->state[1] = out;
	S->state[0] = err;
	return out;
}

/*
 * PID reset functionality if required.
 * Resets all states of PID controller to 0
 */
static __inline void pidreset(pid_t *S)
{
	uint8_t j;
	for(j=0; j<2; j++)
	{
		S->state[i] = 0;
	}
}

act_t stateeval(evt_t e)
{
	statemat_t statetrans = statematrix[currentstate][e];

	currentstate = statetrans.nextState;

	return statetrans.actionToDo;
}
